package com.example.takeawaypro.bean;

public class CookingPin {
    private int cookingpin_id;
    private String cookingpin_name;
    private float price;
    private String picture;
    private String introduce;
    private int cookingxi_id;
    private String cookingxi_name;
    public int getCookingpin_id() {
        return cookingpin_id;
    }

    public void setCookingpin_id(int cookingpin_id) {
        this.cookingpin_id = cookingpin_id;
    }

    public String getCookingpin_name() {
        return cookingpin_name;
    }

    public void setCookingpin_name(String cookingpin_name) {
        this.cookingpin_name = cookingpin_name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getIntroduce() {
        return introduce;
    }

    public void setIntroduce(String introduce) {
        this.introduce = introduce;
    }

    public int getCookingxi_id() {
        return cookingxi_id;
    }

    public void setCookingxi_id(int cookingxi_id) {
        this.cookingxi_id = cookingxi_id;
    }

    public String getCookingxi_name() {
        return cookingxi_name;
    }

    public void setCookingxi_name(String cookingxi_name) {
        this.cookingxi_name = cookingxi_name;
    }




}
